#ifndef SYSTEM_COTS_MCAL_SPI_SPI_PRIV_H_
#define SYSTEM_COTS_MCAL_SPI_SPI_PRIV_H_

#define SPI_MASTER_MODE						0b00010000
#define SPI_SLAVE_MODE						0b00000000
#define SPI_MODE_CLEAR						0b11101111

#define SPI_CLOCK_POLARITY_IDLE_HIGH		0b00000000
#define SPI_CLOCK_POLARITY_IDLE_LOW			0b00001000
#define SPI_CLOCK_POLARITY_CLEAR			0b11110111

#define SPI_CLOCK_PHASE_SAMPLE_FIRST		0b00000000
#define SPI_CLOCK_PHASE_SETUP_FIRST			0b00000100
#define SPI_CLOCK_PHASE_CLEAR				0b11111011

#define SPI_CLOCK_RATE_DIV_4				0b00000000
#define SPI_CLOCK_RATE_DIV_16				0b00000001
#define SPI_CLOCK_RATE_DIV_64				0b00000010
#define SPI_CLOCK_RATE_DIV_128				0b00000011
#define SPI_CLOCK_RATE_CLEAR				0b11111100

#define SPI_ENABLE_DOUBLE_RATE				0b00000001
#define SPI_DISABLE_DOUBLE_RATE				0b00000000
#define SPI_DOUBLE_RATE_CLEAR				0b11111110

#endif /* SYSTEM_COTS_MCAL_SPI_SPI_PRIV_H_ */
